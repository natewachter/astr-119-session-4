import numpy as np

x = 1.0   # floating point variable
y = 2.0   # another floating point variable

# trig functions
print(np.sin(x))   # sin(x)
print(np.cos(x))   # cos(x)
print(np.tan(x))   # tan(x)
print(np.arcsin(x))   # arcsin(x)
print(np.arccos(x))   # arccos(x)
print(np.arctan(x))   # arctan(x)
print(np.arctan2(x,y))   # arctan(x/y)
print(np.rad2deg(x))   # convert rad x to degrees

# hyperbolic functions
print(np.sinh(x))   # sin(x)
print(np.cosh(x))   # cos(x)
print(np.tanh(x))   # tan(x)

# inverse hyperbolic functions
print(np.arcsinh(x))   # arcsin(x)
print(np.arccosh(x))   # arccos(x)
print(np.arctanh(x))   # arctan(x)
